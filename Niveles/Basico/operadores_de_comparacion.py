a = 12
b= 5

#igual
igual = a == b

#diferente 
diferente = a != b

#menor que
menor = a < b

#menor o igual que 
menor_igual = a <= b

#mayor que
mayor = a > b

#mayor o igual que 
mayor_igual = a >= b



print(f"A:{a} es igual que B:{b} = {igual}")
print(f"A:{a} es diferente que B:{b} = {diferente}")
print(f"A:{a} es menor que B:{b} = {menor}")
print(f"A:{a} es menor o igual que B:{b} = {menor_igual}")
print(f"A:{a} es mayor que B:{b} = {mayor}")
print(f"A:{a} es mayor o igual que B:{b} = {mayor_igual}")